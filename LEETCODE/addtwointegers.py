# Given two integers num1 and num2, return the sum of the two integers.

def sum(num1, num2):
    return num1 + num2

# Example usage:
num1 = 5
num2 = 3
print(sum(num1, num2))  # Output: 8

num1 = -2
num2 = 4
print(sum(num1, num2))  # Output: 2

num1 = 0
num2 = 0
print(sum(num1, num2))  # Output: 0